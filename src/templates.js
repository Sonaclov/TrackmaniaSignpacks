/**
 * Get templates by category
 * @param {string} category - Category name
 * @returns {Object} Filtered templates
 */
export function getTemplatesByCategory(category) {
    const filtered = {};
    for (const [name, template] of Object.entries(COMMUNITY_TEMPLATES)) {
        if (template.category === category) {
            filtered[name] = template;
        }
    }
    return filtered;
}

/**
 * Get templates by difficulty
 * @param {string} difficulty - Difficulty level
 * @returns {Object} Filtered templates
 */
export function getTemplatesByDifficulty(difficulty) {
    const filtered = {};
    for (const [name, template] of Object.entries(COMMUNITY_TEMPLATES)) {
        if (template.difficulty === difficulty) {
            filtered[name] = template;
        }
    }
    return filtered;
}

/**
 * Get all categories
 * @returns {Array} Array of unique categories
 */
export function getAllCategories() {
    const categories = new Set();
    for (const template of Object.values(COMMUNITY_TEMPLATES)) {
        categories.add(template.category);
    }
    return Array.from(categories);
}

export default {
    COMMUNITY_TEMPLATES,
    getTemplatesByCategory,
    getTemplatesByDifficulty,
    getAllCategories
};
